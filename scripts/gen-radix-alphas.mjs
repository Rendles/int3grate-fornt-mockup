// Generate alpha-channel Radix scales for the int3grate brand palette.
//
// Algorithm mirrors @radix-ui/colors' `getAlphaColorSrgb`:
//   For each opaque step `T` over background `B`, find (alpha, source S)
//   such that  T = alpha * S + (1 - alpha) * B
//   Per channel, push S toward 0 (when T < B) or 255 (when T > B) to maximise
//   alpha. Take the max across channels, then solve back for S.
//
// Usage:  node scripts/gen-radix-alphas.mjs
//
// Output: CSS-ready `--<scale>-a1 .. -a12: rgba(r, g, b, a);` blocks, one
// per scale-per-theme. Paste into src/prototype/brand-colors.css.

const SCALES = {
  violet: {
    light: ['#FDFCFF', '#F8F4FF', '#EFE5FF', '#E3D2FF', '#D5BBFE', '#C4A1F8', '#AC82EE', '#8B59E0', '#701DFD', '#6315E5', '#5A12CC', '#2C1668'],
    dark:  ['#110A1F', '#170E2B', '#221540', '#2A1755', '#321B6A', '#3D2585', '#4D2DA8', '#5E1FD8', '#701DFD', '#8240FD', '#B89BFF', '#E0D4FF'],
  },
  cyan: {
    light: ['#FAFEFF', '#F0FBFE', '#DCF5FD', '#BFEDFB', '#97E2F8', '#6BD2F2', '#38BFE8', '#11A6D6', '#01C9FA', '#00A8D4', '#0C7B9C', '#0A3947'],
    dark:  ['#06141C', '#081A24', '#082B3A', '#073A50', '#064867', '#06587C', '#066D97', '#0386BB', '#01C9FA', '#36D5FB', '#7FE6FE', '#D6F6FF'],
  },
  orange: {
    light: ['#FFFDF9', '#FFF7EA', '#FFEDCD', '#FFE0A8', '#FFCF7A', '#FCBC4B', '#F2A726', '#DE9214', '#FD9C12', '#E58A08', '#B26800', '#4F2D00'],
    dark:  ['#1A1006', '#221608', '#38210A', '#4A2A0A', '#5C320A', '#6F3E08', '#874C06', '#BB6C00', '#FD9C12', '#FFB13E', '#FFCB7A', '#FFE5C4'],
  },
}

const BG = {
  light: '#F7F8FA', // Mist White
  dark:  '#05070A', // Core Black
}

function hexToRgb(hex) {
  const h = hex.replace('#', '')
  return [parseInt(h.slice(0, 2), 16), parseInt(h.slice(2, 4), 16), parseInt(h.slice(4, 6), 16)]
}

// Round alpha to 4 decimals, keep numeric stability.
function round(x, places = 4) {
  return Math.round(x * 10 ** places) / 10 ** places
}

// Radix algorithm: derive (sourceColor, alpha) so blend-on-bg equals target.
function alphaFromOpaque(targetHex, bgHex) {
  const [tR, tG, tB] = hexToRgb(targetHex)
  const [bR, bG, bB] = hexToRgb(bgHex)

  // For each channel, alpha needed if S is pushed to limit (0 if T<B, 255 if T>B):
  function alphaForChannel(t, b) {
    if (t === b) return 0
    return t < b ? (b - t) / b : (t - b) / (255 - b)
  }
  const a = Math.max(alphaForChannel(tR, bR), alphaForChannel(tG, bG), alphaForChannel(tB, bB))
  if (a === 0) {
    // Step equals background — emit a zero-alpha sentinel using brand source.
    return { r: 0, g: 0, b: 0, a: 0 }
  }

  // Solve for source per channel: T = a*S + (1-a)*B  =>  S = (T - (1-a)*B) / a
  const sR = Math.round((tR - bR * (1 - a)) / a)
  const sG = Math.round((tG - bG * (1 - a)) / a)
  const sB = Math.round((tB - bB * (1 - a)) / a)

  return {
    r: Math.max(0, Math.min(255, sR)),
    g: Math.max(0, Math.min(255, sG)),
    b: Math.max(0, Math.min(255, sB)),
    a: round(a, 4),
  }
}

function emit(scale, theme) {
  const opaque = SCALES[scale][theme]
  const bg = BG[theme]
  const lines = []
  for (let i = 0; i < 12; i++) {
    const { r, g, b, a } = alphaFromOpaque(opaque[i], bg)
    const varName = `--${scale}-a${i + 1}`
    lines.push(`  ${varName.padEnd(12)}: rgba(${r}, ${g}, ${b}, ${a});`)
  }
  return lines.join('\n')
}

const out = []
out.push('/* ============================================================')
out.push('   ALPHA SCALES — generated by scripts/gen-radix-alphas.mjs')
out.push('   Backgrounds: light=Mist White #F7F8FA, dark=Core Black #05070A.')
out.push('   ============================================================ */\n')

out.push('/* ----- LIGHT MODE ALPHAS ----- */')
out.push(':root,\n.light,\n.light-theme {')
for (const scale of ['violet', 'cyan', 'orange']) {
  out.push(`  /* ${scale} */`)
  out.push(emit(scale, 'light'))
}
out.push('}\n')

out.push('/* ----- DARK MODE ALPHAS ----- */')
out.push('.dark,\n.dark-theme {')
for (const scale of ['violet', 'cyan', 'orange']) {
  out.push(`  /* ${scale} */`)
  out.push(emit(scale, 'dark'))
}
out.push('}')

