function row(id: string, label: string, total: number, runs: number, tokensIn: number, tokensOut: number): SpendRow {
  return {
    id,
    label,
    total_usd: total,
    total_tokens_in: tokensIn,
    total_tokens_out: tokensOut,
    run_count: runs,
    spend_date: new Date().toISOString().slice(0, 10),
  }
}

