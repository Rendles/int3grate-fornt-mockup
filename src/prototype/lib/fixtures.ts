function step(p: {
  id: string
  kind: RunStep['step_type']
  status: string
  model?: string
  tool?: string
  input?: Record<string, unknown>
  output?: Record<string, unknown>
  cost?: number
  tokens_in?: number
  tokens_out?: number
  duration_ms?: number
  created_minutes_ago: number
  completed_minutes_ago?: number | null
}): RunStep {
  return {
    id: p.id,
    step_type: p.kind,
    status: p.status,
    model_name: p.model ?? null,
    tool_name: p.tool ?? null,
    input_ref: p.input ?? null,
    output_ref: p.output ?? null,
    cost_usd: p.cost ?? null,
    tokens_in: p.tokens_in ?? null,
    tokens_out: p.tokens_out ?? null,
    duration_ms: p.duration_ms ?? null,
    created_at: mins(p.created_minutes_ago),
    completed_at:
      p.completed_minutes_ago === null
        ? null
        : mins(p.completed_minutes_ago ?? p.created_minutes_ago),
  }
}

