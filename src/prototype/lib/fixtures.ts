function sparkSeries(base: number, trend: 'up' | 'down' | 'flat' | 'spike'): number[] {
  const out: number[] = []
  for (let i = 0; i < 14; i++) {
    let v = base
    if (trend === 'up') v = base * (0.6 + (i / 14) * 0.7)
    if (trend === 'down') v = base * (1.2 - (i / 14) * 0.7)
    if (trend === 'spike' && i === 10) v = base * 1.9
    v *= 0.85 + (Math.sin(i * 7.1 + base) * 0.5 + 0.5) * 0.35
    out.push(Math.max(0.05, v))
  }
  return out
}

function computeSpend(range: SpendRange, group_by: SpendGroupBy): SpendDashboard {
  const multiplier: Record<SpendRange, number> = { '1d': 0.03, '7d': 0.22, '30d': 1, '90d': 2.8 }
  const m = multiplier[range]
  const windowLabel: Record<SpendRange, string> = {
    '1d': 'last 24h',
    '7d': 'last 7 days',
    '30d': 'last 30 days',
    '90d': 'last 90 days',
  }
  const today = new Date().toISOString().slice(0, 10)

  const byAgentItems: SpendRow[] = agents
    .filter(a => (a.monthly_spend_usd ?? 0) > 0)
    .map(a => {
      const total = (a.monthly_spend_usd ?? 0) * m
      const tokens_in = (a.runs_7d ?? 0) * 4500 * m * 3
      const tokens_out = (a.runs_7d ?? 0) * 1200 * m * 3
      return {
        id: a.id,
        label: a.name,
        total_usd: total,
        tokens_in: Math.round(tokens_in),
        tokens_out: Math.round(tokens_out),
        run_count: Math.round((a.runs_7d ?? 0) * m * 4.2),
        spend_date: today,
        cap_usd: a.monthly_spend_cap_usd ?? null,
        trend: sparkSeries(total / 14, 'up'),
        delta_pct: 8 + (a.id.charCodeAt(4) % 18),
        kind: 'agent',
        sub_label: a.owner_team,
      }
    })

  const userAggregate: Record<string, { total: number; tokens_in: number; tokens_out: number; runs: number; label: string; sub: string }> = {}
  allUsers().forEach(u => {
    userAggregate[u.id] = {
      total: 0,
      tokens_in: 0,
      tokens_out: 0,
      runs: 0,
      label: u.name,
      sub: u.team ?? u.domain_name ?? '',
    }
  })
  agents.forEach(a => {
    const row = userAggregate[a.owner_user_id]
    if (!row) return
    row.total += (a.monthly_spend_usd ?? 0) * m
    row.tokens_in += (a.runs_7d ?? 0) * 4500 * m * 3
    row.tokens_out += (a.runs_7d ?? 0) * 1200 * m * 3
    row.runs += (a.runs_7d ?? 0) * 4.2 * m
  })
  const byUserItems: SpendRow[] = Object.entries(userAggregate)
    .filter(([, v]) => v.total > 0)
    .map(([id, v]) => ({
      id,
      label: v.label,
      total_usd: v.total,
      tokens_in: Math.round(v.tokens_in),
      tokens_out: Math.round(v.tokens_out),
      run_count: Math.round(v.runs),
      spend_date: today,
      trend: sparkSeries(v.total / 14, 'up'),
      delta_pct: 3 + (id.charCodeAt(4) % 22),
      kind: 'user',
      sub_label: v.sub,
    }))

  const items = group_by === 'agent' ? byAgentItems : byUserItems
  const total_usd = items.reduce((s, r) => s + r.total_usd, 0)

  return {
    range,
    group_by,
    total_usd,
    items: items.sort((a, b) => b.total_usd - a.total_usd),
    window_label: windowLabel[range],
    total_runs: items.reduce((s, r) => s + r.run_count, 0),
    total_spend_delta_pct: 12.4,
    total_runs_delta_pct: 22.1,
    pending_approvals: approvals.filter(a => a.status === 'pending').length,
    avg_cost_per_run_usd: items.reduce((s, r) => s + r.run_count, 0) > 0
      ? total_usd / items.reduce((s, r) => s + r.run_count, 0)
      : 0,
    burn_per_day: sparkSeries(total_usd / 17, 'up'),
    cap_usd: 6000 * m,
  }
}

export function getSpendDashboard(range: SpendRange, group_by: SpendGroupBy): SpendDashboard {
  return computeSpend(range, group_by)
}
