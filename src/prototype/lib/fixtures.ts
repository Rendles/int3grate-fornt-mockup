let stepCounter = 0
function mkStep(
  runId: string,
  kind: RunStep['step_type'],
  status: RunStep['status'],
  title: string,
  detail: string,
  opts: Partial<RunStep> = {}
): RunStep {
  stepCounter++
  const createdAt = opts.created_at ?? mins(30 - stepCounter)
  return {
    id: `stp_${runId}_${stepCounter.toString().padStart(3, '0')}`,
    run_id: runId,
    step_index: opts.step_index ?? stepCounter,
    step_type: kind,
    status,
    model_name: opts.model_name ?? (kind === 'llm_call' ? 'gpt-5-mini' : null),
    tool_name: opts.tool_name ?? (kind === 'tool_call' ? 'tool' : null),
    input_ref: opts.input_ref ?? `io_${stepCounter}_in`,
    output_ref: opts.output_ref ?? `io_${stepCounter}_out`,
    cost_usd: opts.cost_usd ?? (kind === 'llm_call' ? 0.01 + Math.random() * 0.04 : 0),
    tokens_in: opts.tokens_in ?? (kind === 'llm_call' ? 400 + Math.floor(Math.random() * 1200) : null),
    tokens_out: opts.tokens_out ?? (kind === 'llm_call' ? 120 + Math.floor(Math.random() * 400) : null),
    duration_ms: opts.duration_ms ?? 600 + Math.floor(Math.random() * 3000),
    created_at: createdAt,
    completed_at: status === 'pending' || status === 'running' ? null : createdAt,
    title,
    detail,
    payload: opts.payload,
    approval_id: opts.approval_id,
  }
}

function buildSteps(runId: string, builder: (mk: (kind: RunStep['step_type'], status: RunStep['status'], title: string, detail: string, opts?: Partial<RunStep>) => RunStep) => RunStep[]): RunStep[] {
  stepCounter = 0
  const local = (kind: RunStep['step_type'], status: RunStep['status'], title: string, detail: string, opts: Partial<RunStep> = {}) =>
    mkStep(runId, kind, status, title, detail, opts)
  return builder(local).map((s, idx) => ({ ...s, step_index: idx + 1 }))
}

